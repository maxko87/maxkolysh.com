// Helper to generate display name from fund data
export const generateDisplayName = (fundName: string, vintage: number, moic: number, isNotable: boolean = false): string => {
  const star = isNotable ? '‚≠ê ' : '';
  const moicFormatted = moic.toFixed(2).replace(/\.?0+$/, '');
  return `${star}${fundName} (${vintage}) - ${moicFormatted}x`;
};

// Helper to create fund data from preset
